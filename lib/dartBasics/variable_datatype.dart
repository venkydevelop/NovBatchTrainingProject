void main() {
  // variables - store data by using name. var,final,const,late,dynamic

  // var

  var name = '12'; // fixed the data type
  name = '123';
  print(name);

  //dynamic

  dynamic name1 = 'Hello';
  name1 = 123;

  // final - set once

  final String stud;
  stud = "";

  // const - compile time

  const value = 12.50;

  late String myValue;

  // Data Types - String,bool,int,float, double

  String myName = 'Test';
  int val = 12;
  double myDouble = 12;
  bool isLogin = true;
}
